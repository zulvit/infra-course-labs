package main

import "fmt"

func main() {
	// Указатель — это значение, хранящее адрес блока памяти.
	// В Go указатель не является «магическим» объектом:
	// это обычное значение типа *T, где T — тип, на который он ссылается.
	// Значение по умолчанию для указателя — nil.

	// Оператор & (амперсанд) возвращает адрес переменной.
	var x int
	pntr := &x
	fmt.Println("Адрес x:", pntr) // 0xc00001c080 (конкретное значение зависит от запуска)

	// Оператор * (разыменование) позволяет получить
	// значение по известному адресу.
	x = 64
	fmt.Println("*pntr:", *pntr) // 64

	// Через указатель можно изменить значение оригинальной переменной.
	// Это единственный способ «мутировать на расстоянии» в Go:
	// языке без ссылок и конструкций ref/out.
	*pntr = 31
	fmt.Println("*pntr после изменения:", *pntr) // 31
	fmt.Println("x после изменения:    ", x)      // 31

	// uintptr — целочисленный тип для хранения адреса.
	// uintptr хранит числовое значение адреса.
	// В отличие от *T, GC не отслеживает uintptr:
	// если объект доступен только через uintptr, GC может его собрать.
	// Используется только в связке с unsafe — для обычного кода не нужен.
	var pntr1 *int
	var pntr2 uintptr
	fmt.Println("nil-указатель:", pntr1) // <nil>
	fmt.Println("uintptr zero: ", pntr2) // 0

	// Функция new()
	// new(T) выделяет память для значения типа T,
	// заполняет его zero value и возвращает *T.
	// Это синтаксический сахар — эквивалент:
	//   var v T
	//   p := &v
	var intValue int
	p1 := &intValue
	p2 := new(int)
	fmt.Println("&intValue:", p1) // адрес
	fmt.Println("new(int): ", p2) // другой адрес

	// Оба указателя ведут к zero value (0):
	fmt.Println("*p1 =", *p1) // 0
	fmt.Println("*p2 =", *p2) // 0

	// Nil-указатель: проверка перед использованием
	// Разыменование nil-указателя вызывает panic.
	// Профессиональная привычка: всегда проверять перед разыменованием.
	var nilPtr *int
	if nilPtr != nil {
		fmt.Println(*nilPtr)
	} else {
		fmt.Println("nilPtr is nil - разыменование запрещено")
	}

	// Указатели и функции
	// Передача указателя позволяет функции изменить оригинальное значение.
	val := 100
	increment(&val)
	fmt.Println("val после increment:", val) // 101

	// КЛЮЧЕВОЕ ОТЛИЧИЕ от C/C++
	// Go НЕ поддерживает арифметику указателей.
	// Нельзя сделать pntr++ или pntr + 4.
	// Это сознательное ограничение для безопасности памяти.
	// Адресная арифметика возможна только через пакет unsafe!	
}

// increment увеличивает значение по указателю на 1.
// Pointer receiver используется для мутации исходного значения.
func increment(p *int) {
	*p++
}
