package main

import "fmt"

func main() {
	// Map — структура для хранения пар ключ-значение.
	// В других языках: хэш-таблица, словарь, ассоциативный массив.
	// Операции чтения и записи — O(1) в среднем.

	// Способы создания

	// 1. Объявление без инициализации — nil map.
	// Чтение из nil map — безопасно (вернёт zero value).
	// Запись в nil map — PANIC!
	var nilMap map[int]string
	fmt.Println("nilMap:", nilMap)           // map[]
	fmt.Println("nilMap == nil:", nilMap == nil) // true
	// nilMap[1] = "boom" // panic: assignment to entry in nil map

	// 2. Литеральная инициализация — пустая map:
	m1 := map[int]string{}
	fmt.Println("m1:", m1) // map[]

	// 3. make() с подсказкой размера — рекомендуемый способ.
	// Подсказка (hint) помогает Go выделить память заранее,
	// уменьшая количество реаллокаций при росте.
	m2 := make(map[int]string, 10)
	fmt.Println("m2:", m2) // map[]

	// 4. Литерал с элементами:
	m3 := map[int]string{1: "hello", 2: "world"}
	fmt.Println("m3:", m3) // map[1:hello 2:world]

	// ── Запись и чтение ──
	m3[3] = "!"
	word := m3[1]
	fmt.Println("m3[1]:", word) // hello
	fmt.Println("m3:", m3)      // map[1:hello 2:world 3:!]

	// Comma-ok idiom
	// При чтении несуществующего ключа Go возвращает zero value типа.
	// Это опасно для bool: m[key] вернёт false и для «false» и для «не существует».
	// Решение: вторая переменная ok.
	existedIDs := map[int64]bool{1: true, 2: true}
	idExists, ok := existedIDs[2]
	fmt.Printf("key=2: value=%v, exists=%v\n", idExists, ok) // true, true

	idExists, ok = existedIDs[225]
	fmt.Printf("key=225: value=%v, exists=%v\n", idExists, ok) // false, false

	// delete() — удаление ключа
	engToRus := map[string]string{"hello": "привет", "world": "мир"}
	delete(engToRus, "world")
	fmt.Println("после delete:", engToRus) // map[hello:привет]

	// Удаление несуществующего ключа — безопасно, не вызывает panic.
	delete(engToRus, "nonexistent")

	// Reference semantics
	// Map ВСЕГДА передаётся «по ссылке» (через внутренний pointer).
	// Изменения в функции видны в вызывающем коде!
	m := map[int]string{1: "hello", 2: "world"}
	modifyMap(m)
	fmt.Println("m после modifyMap:", m) // map[1:changed 2:world 200:added]

	// range по map
	// for k, v := range m — итерация по всем парам.
	idToName := map[int64]string{1: "Alex", 2: "Dan", 3: "George"}
	for id, name := range idToName {
		fmt.Printf("  id: %d, name: %s\n", id, name)
	}

	// ПОРЯДОК ИТЕРАЦИИ РАНДОМИЗИРОВАН
	// Go специально рандомизирует порядок при range по map.
	// Это сделано, чтобы разработчики не зависели от порядка.
	// Каждый запуск может дать РАЗНЫЙ порядок ключей!
	numExistence := make(map[int]bool, 10)
	for i := 0; i < 10; i++ {
		numExistence[i] = true
	}
	fmt.Print("ключи в случайном порядке: ")
	for num := range numExistence {
		fmt.Print(num, " ")
	}
	fmt.Println()
	// Вывод при каждом запуске разный: 8 1 2 3 6 7 9 0 4 5
}

// modifyMap демонстрирует reference semantics.
// Все изменения видны в оригинальной map.
func modifyMap(m map[int]string) {
	m[200] = "added"
	m[1] = "changed"
}

// ============================================================================
// КАК УСТРОЕН MAP ВНУТРИ GO
// https://go.dev/blog/swisstable
// ============================================================================
//
// С Go 1.24 map полностью переписан на основе Swiss Table.
// До этого (Go 1.0–1.23) использовалась своя реализация на основе bucket-chain.
//
// СТАРАЯ РЕАЛИЗАЦИЯ (Go 1.0–1.23): bucket + overflow 
//
// Переменная типа map[K]V — это указатель на структуру runtime.hmap:
//
//   type hmap struct {
//       count     int            // количество элементов
//       flags     uint8          // состояние (итерация, запись, ...)
//       B         uint8          // log2(кол-во бакетов), т.е. 2^B бакетов
//       noverflow uint16         // приблизительное число overflow-бакетов
//       hash0     uint32         // seed для хэш-функции (рандомизация)
//       buckets   unsafe.Pointer // массив из 2^B бакетов
//       oldbuckets unsafe.Pointer // старый массив при grow (incremental)
//       ...
//   }
//
// Каждый бакет (bmap) вмещал до 8 пар key-value:
//
//   type bmap struct {
//       tophash [8]uint8    // верхние 8 бит хэша каждого ключа (фильтр)
//       // затем идут 8 ключей подряд, потом 8 значений подряд
//       // (не key1,val1,key2,val2 — чтобы избежать padding)
//       overflow *bmap      // указатель на overflow-бакет (цепочка)
//   }
//
// Алгоритм поиска:
//   1. hash(key) → берём младшие B бит → номер бакета
//   2. Берём старшие 8 бит хэша (tophash) → сравниваем с tophash[0..7]
//   3. Если tophash совпал → сравниваем сам ключ
//   4. Если не нашли → идём в overflow-бакет (связный список)
//
// Load factor = 6.5 элементов на бакет (81.25%).
// При превышении — grow: удваиваем кол-во бакетов.
// Grow был ИНКРЕМЕНТАЛЬНЫМ: не всё сразу, а по чуть-чуть при каждой записи.
//
//  НОВАЯ РЕАЛИЗАЦИЯ (Go 1.24+): Swiss Table 
//
// Swiss Table — это open-addressing хэш-таблица, разработанная
// инженерами Google (Abseil C++, 2017). Go 1.24 полностью перешёл на неё.
// 
//
// Ключевые отличия от старой реализации:
//
// 1. ГРУППЫ ПО 8 СЛОТОВ + CONTROL WORD
//
//    Вместо бакетов с overflow-списками — плоский массив слотов,
//    разбитый на группы по 8. Каждая группа имеет 64-битное
//    контрольное слово (control word) — 8 байт метаданных:
//
//    ┌───────────────────────────────────────────────┐
//    │ Control Word (8 байт): [h2][h2][h2][__][h2]...│
//    ├───────────────────────────────────────────────┤
//    │ Slots: [K1,V1] [K2,V2] [K3,V3] [empty] ...    │
//    └───────────────────────────────────────────────┘
//
//    Каждый байт control word хранит:
//    - 0x80 (empty)   — слот пуст
//    - 0xFE (deleted) — слот удалён
//    - 0x00..0x7F     — младшие 7 бит хэша ключа (h2-fingerprint)
//
// 2. ДВУХУРОВНЕВЫЙ ХЭШ
//
//    hash(key) разбивается на две части:
//    - h1 (старшие 57 бит) — выбор группы: h1 % numGroups
//    - h2 (младшие 7 бит)  — fingerprint для быстрой фильтрации
//
// 3. ПАРАЛЛЕЛЬНЫЙ ПОИСК (SIMD-подобный)
//
//    При поиске ключа:
//    a) h1 → выбираем группу
//    b) Сравниваем h2 сразу со всеми 8 байтами control word
//       (через побитовые операции или SIMD-инструкции)
//    c) Получаем битовую маску совпадений — кандидаты
//    d) Только для кандидатов сравниваем полные ключи
//    e) Если группа не подошла — пробируем следующую
//
//    Это даёт проверку 8 слотов за одну операцию!
//
// 4. EXTENDIBLE HASHING (инкрементальный рост)
//
//    Go не может остановить мир для перехэширования 1 ГБ данных.
//    Решение: map состоит из МНОЖЕСТВА независимых маленьких таблиц.
//    Каждая таблица — максимум 1024 записи.
//    При росте разделяется ОДНА таблица, а не вся map.
//    Верхние биты хэша определяют, в какую таблицу идёт ключ.
//
// 5. ИТЕРАЦИЯ + МОДИФИКАЦИЯ
//
//    В Go (в отличие от C++/Rust) можно модифицировать map во время range.
//    Это самая сложная часть реализации.
//    Итератор хранит ссылку на старую версию таблицы для порядка обхода,
//    но проверяет актуальные данные в новой таблице.
//
// Советы в итоге:
// 1. Всегда используй make(map[K]V, hint) если знаешь размер заранее
// 2. Map НЕ потокобезопасна — для конкурентного доступа: sync.Map или sync.RWMutex
// 3. Ключом может быть только comparable тип (нельзя: slice, map, func)
// 4. Порядок итерации рандомизирован — это by design
// 5. Map — reference type (передаётся «по ссылке» через внутренний pointer)
// 6. Нельзя брать адрес значения в map: &m[key] — ошибка компиляции,
//    потому что при росте map данные перемещаются в памяти
