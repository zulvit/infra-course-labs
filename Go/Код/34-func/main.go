package main

import (
	"errors"
	"fmt"
)

func main() {
	// Функции в Go объявляются через ключевое слово func.
	// Структура: func имя(параметры) (возвраты) { тело }

	// Базовый вызов:
	result := multiply(6, 7)
	fmt.Println("6 * 7 =", result) // 42

	// Сокращённая запись типов параметров
	// Если несколько параметров подряд имеют одинаковый тип,
	// тип можно указать один раз: (x, y int) вместо (x int, y int).
	fmt.Println("add(10, 20):", add(10, 20)) // 30

	// Multiple return values
	// Go позволяет возвращать несколько значений.
	// Чаще всего (результат, ошибка) — каноническая идиома.
	res, err := divide(10, 3)
	if err != nil {
		fmt.Println("Ошибка:", err)
	} else {
		fmt.Println("10 / 3 =", res) // 3
	}

	// Деление на ноль — ошибка:
	_, err = divide(10, 0)
	if err != nil {
		fmt.Println("Ошибка:", err) // cannot divide by zero
	}

	// Named return values (именованные возвраты)
	// Go позволяет дать имена возвращаемым значениям.
	// Они становятся локальными переменными и инициализируются zero value.
	// Naked return (return без аргументов) возвращает их текущие значения.
	fmt.Println("multiplyNamed(5, 8):", multiplyNamed(5, 8)) // 40

	// ВНИМАНИЕ: именованные возвраты — плохая практика в большинстве случаев.
	// Причины:
	// 1. Увеличенное расстояние между объявлением и использованием переменной —
	//    сложнее читать и понимать код.
	// 2. Переменная может быть переопределена в теле функции,
	//    что приводит к неожиданному поведению.
	// 3. Naked return неявно возвращает значения — не видно, что возвращается.
	//
	// Единственный допустимый случай — defer с модификацией ошибки.

	// Экспорт функций
	// Multiply (заглавная) — экспортируемая (публичная), доступна из других пакетов.
	// divide (строчная) — неэкспортируемая (приватная), только в текущем пакете.
	// Это НЕ конвенция, а правило языка, проверяемое компилятором.
	//
	// package math
	// func Multiply(x, y int) int { ... }  // публичная: math.Multiply(5, 7)
	// func divide(x, y int) int { ... }    // приватная: вне пакета недоступна

	// Функция как first-class citizen
	// Функции можно присваивать переменным и передавать как аргументы.
	op := multiply
	fmt.Println("op(3, 4):", op(3, 4)) // 12

	// Передача функции как аргумента:
	fmt.Println("apply(5, 3, multiply):", apply(5, 3, multiply)) // 15
	fmt.Println("apply(5, 3, add):     ", apply(5, 3, add))      // 8
}

// multiply — простая функция с одним возвратом.
func multiply(x, y int) int {
	return x * y
}

// add — сокращённая запись параметров одного типа.
func add(x, y int) int {
	return x + y
}

// divide — множественный возврат (результат, ошибка).
// Это каноническая идиома Go для функций, которые могут завершиться с ошибкой.
func divide(x, y int) (int, error) {
	if y == 0 {
		return 0, errors.New("cannot divide by zero")
	}
	return x / y, nil
}

// multiplyNamed — именованный возврат (res int).
// АНТИ-ПАТТЕРН: naked return скрывает возвращаемое значение.
// Показан для обучения — в продакшн-коде избегайте.
func multiplyNamed(x, y int) (res int) {
	res = x * y
	return // naked return — неявно возвращает res
}

// apply принимает функцию как аргумент — демонстрация first-class functions.
func apply(a, b int, op func(int, int) int) int {
	return op(a, b)
}
