package main

import "fmt"

func main() {
	// Массив - коллекция однотипных значений ФИКСИРОВАННОЙ длины.
	// Длина массива - часть его типа. [3]int и [4]int - РАЗНЫЕ типы.
	// Это фундаментальное отличие от слайсов.

	// Способы объявления:
	var a [3]int                                    // zero value: [0 0 0]
	var b [3]string = [3]string{"one", "two", "three"} // явная инициализация
	c := [5]int{}                                   // zero value: [0 0 0 0 0]
	fmt.Println("a:", a) // [0 0 0]
	fmt.Println("b:", b) // [one two three]
	fmt.Println("c:", c) // [0 0 0 0 0]

	// Оператор [...] - компилятор сам подсчитает количество элементов:
	d := [...]int{1, 2, 3}
	fmt.Println("d:", d) // [1 2 3]

	// Индексная инициализация - можно задать значение по конкретному индексу:
	e := [3]int{1: 12}
	fmt.Println("e:", e) // [0 12 0]

	// Чтение и запись через квадратные скобки
	nums := [3]int{1, 2, 3}
	fmt.Println("nums[1]:", nums[1]) // 2
	nums[2] = 33
	fmt.Println("nums:", nums) // [1 2 33]

	// Выход за границы массива - ошибка КОМПИЛЯЦИИ (если индекс - константа):
	// nums[3] = 10 // invalid array index 3 (out of bounds for 3-element array)

	// Длина как часть типа
	// Это означает, что функция, принимающая [3]int,
	// НЕ может принять [2]int или [4]int.
	arr3 := [3]int{}
	arr2 := [2]int{}
	fmt.Printf("тип arr3: %T\n", arr3) // [3]int
	fmt.Printf("тип arr2: %T\n", arr2) // [2]int
	// Нельзя: arr3 = arr2 — несовместимые типы

	// Value semantics (передача по значению)
	// Массивы копируются при присваивании и при передаче в функцию.
	// Это значит: изменения внутри функции НЕ затрагивают оригинал.
	original := [3]int{1, 2, 3}
	modifyArr(original)
	fmt.Println("original после modifyArr:", original) // [1 2 3] — не изменился!

	// Копирование при присваивании:
	copy1 := original
	copy1[0] = 999
	fmt.Println("original:", original) // [1 2 3]
	fmt.Println("copy1:   ", copy1)    // [999 2 3]

	// len() для массивов
	// Всегда возвращает фиксированное значение (известно на этапе компиляции).
	fmt.Println("len([5]int{1,2,3}):", len([5]int{1, 2, 3})) // 5
	fmt.Println("len([10]int{}):    ", len([10]int{}))        // 10

	// Многострочная запись
	// Go требует запятую после последнего элемента при многострочной записи.
	// Это сделано для чистых diff-ов и удобства при добавлении/удалении.
	grades := [5]int{
		98,
		93,
		77,
		82,
		83, // ← запятая обязательна!
	}
	fmt.Println("grades:", grades)

	// Итерация через range
	for i, v := range grades {
		fmt.Printf("  grades[%d] = %d\n", i, v)
	}

	// Когда массив, а когда слайс?
	// Массив - когда размер известен на этапе компиляции и не меняется.
	// На практике массивы в Go используются редко.
	// Слайсы (проект 32) покрывают 99% случаев.
}

// modifyArr принимает массив ПО ЗНАЧЕНИЮ - это копия.
// Изменения внутри функции не затрагивают оригинал.
func modifyArr(nums [3]int) {
	nums[0] = 35
}
